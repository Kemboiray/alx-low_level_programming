#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: integer
 * @b: integer
 *
 * Return: sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: integer
 * @b: integer
 *
 * Return: answer.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: integer
 * @b: integer
 *
 * Return: multiple.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: integer
 * @b: integer
 *
 * Return: answer.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulate two integers
 * @a: integer
 * @b: integer
 *
 * Return: modulo.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
